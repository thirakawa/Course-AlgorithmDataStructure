"""selection_struct.py

-----------------------------------------------------------------------
選択構造（Selection structure）のサンプルプログラム。

条件分岐を含む演算例を示しています。

このプログラムでは入力された値を比較し、必要に応じて処理の流れを分岐
させる構造を示します。ここでは `if` / `elif` / `else` を使い、
二つの数の大小関係を判定して異なるメッセージを出力します。
"""


# ユーザーが2つの数値を入力
a = float(input("一つ目の数字を入力してください: "))
b = float(input("二つ目の数字を入力してください: "))

# 選択構造を使用して大小をチェック
if a > b:
    comparison = "最初の数が大きいです。"
elif a < b:
    comparison = "二番目の数が大きいです。"
else:
    comparison = "両方の数は等しいです。"

# 結果を表示
print(comparison)

print("プログラム終了")
