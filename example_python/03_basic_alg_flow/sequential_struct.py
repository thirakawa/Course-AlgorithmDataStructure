"""sequential_struct.py

-----------------------------------------------------------------------
順次構造（Sequential structure）のサンプルプログラム。

上から下へ一連の処理を順番に実行し、分岐や繰り返しを含まない
単純な演算例を示しています。

このスクリプトではユーザーから2つの数を受け取り、加算と減算を順に
計算して結果を表示します。
"""


# ユーザーが2つの数値を入力
a = float(input("一つ目の数字を入力してください: "))
b = float(input("二つ目の数字を入力してください: "))

# 足し算と割り算を一つずつ実行
total = a + b
diff = a - b

# 結果を表示
print(f"あなたが入力した数値: {a} and {b}")
print(f"和: {total}")
print(f"差: {diff}")

print("プログラム終了")
