/*
 * repeating_struct_robot.c
 *
 * 順次構造（Sequential structure）のサンプルプログラム。
 * 
 * 上から下へ一連の処理を順番に実行し、分岐や繰り返しを含まない
 * 単純な演算例を示しています。
 * 
 * このスクリプトでは講義「03-アルゴリズムを表現する方法」で解説した
 * 「ロボットのおつかい」で紹介した選択構造のプログラムを実行します。
 */

#include <stdio.h>

int main(void) {
    printf("プログラム開始\n");

    printf("スーパーマーケットでしらたき（もしくは糸こんにゃく）を買いました。\n");
    printf("まだお金が余っているのでどら焼きを買おうと思います（1個120円）。\n");

    // 手元に残っているお金の金額を入力
    int a;
    printf("手元にはいくらお金が余っていますか？: ");
    scanf("%d", &a);

    printf("いま %d 円お金があります。\n", a);

    // whileループを使用して120円以上あればどら焼きを買う処理を繰り返す
    while (a >= 120) {
        printf("120円以上あるのでどら焼きを一つ買います。\n");
        a -= 120;
        printf("残りのお金は %d 円です。\n", a);
    }

    printf("120円以上お金がないのでどら焼きは買えません。\n");
    printf("家に帰ります。\n");

    printf("プログラム終了\n");

    return 0;
}